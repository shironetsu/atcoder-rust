fn round(x: i64, i: u32)->i64{
    let n = 10i64.pow(i+1);
    let r = x.rem_euclid(n);
    if r < n/2 {
        x - r
    } else {
        x - r + n
    }
}

#[fastout]
fn main() {
    input!{
        mut X: i64,
        K: u32
    }

    for i in 0..=K-1{
        X = round(X, i);
    }

    println!("{}", X);

    
}
