#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
        X: i64,
    }

    let mut C = vec![0;N];
    let mut A = vec![vec![0;M];N];

    for i in 0..N{
        input!{
            c: i64,
        }
        C[i] = c;
        input!{
            a: [i64;M],
        }
        A[i] = a;
    }

    let inf = 10i64.pow(18);
    let mut ans = inf;
    for s in 0..(1<<N){
        let cost = (0..N).filter(|&i|(s>>i)&1==1).map(|i|C[i]).sum::<i64>();
        let ok = (0..M).map(|j|{
            (0..N).filter(|&i|(s>>i)&1==1).map(|i|A[i][j]).sum::<i64>()
        }).find(|&a|a<X).is_none();
        if ok {
            ans.chmin(cost);
        }
    }

    if ans == inf {
        println!("{}", -1);
    } else {
        println!("{}", ans);
    }

    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
