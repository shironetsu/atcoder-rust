#[fastout]
fn main() {
    input!{
        N: usize,
        K: i64,
        A: [Usize1;N],
    }

    let mut nth = vec![None;N];
    let mut path = vec![];
    let mut x = 0;
    let mut a = 0;
    for i in 0.. {
        if let Some(j) = nth[x]{
            a = j;
            break;
        } else {
            nth[x] = Some(i);
            path.push(x);
            x = A[x];
        }
    }

    let ans = if K < a as i64 {
        path[K as usize]
    } else {
        let r = (K - a as i64) % (path.len() - a) as i64;
        path[a+r as usize]
    };

    println!("{}", ans + 1);



    
}
