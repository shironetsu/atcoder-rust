#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
        AB: [(Usize1, Usize1);M],
        K: usize,
        CD: [(Usize1,Usize1);K],
    }

    let mut ans = 0;
    for s in 0..1<<K{
        let mut p = vec![false;N];
        for (i, &(c, d)) in CD.iter().enumerate(){
            if (s>>i) & 1 == 0 {
                p[c] = true;
            } else {
                p[d] = true;
            }
        }

        let t = AB.iter().filter(|&&(a, b)|p[a] && p[b]).count();
        ans.chmax(t);
    }

    println!("{}", ans);
    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
