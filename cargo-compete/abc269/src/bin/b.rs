#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        S: [Chars;10],
    }
    let T = vec![vec!['.';10];10];
    for a in 0..10{
        for b in a..10{
            for c in 0..10{
                for d in c..10{
                    let mut T = T.clone();
                    for i in a..=b{
                        for j in c..=d{
                            T[i][j] = '#';
                        }
                    }
                    if S == T {
                        println!("{} {}", a+1, b+1);
                        println!("{} {}", c+1, d+1);
                    }
                }
            }
        }
    }
    
}
