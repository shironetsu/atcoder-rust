#[fastout]
fn main() {
    input!{
        N: usize,
        p: [usize;N],
    }

    let mut d = vec![0;N];
    for i in 0..N{
        d[i] = (p[i] as i32 - i as i32).rem_euclid(N as i32) as usize;
    }
    let mut c = vec![0;N];
    for i in 0..N{
        c[d[i]] += 1;
    }
    let mut ans = 0;
    for i in 0..N{
        let u = c[i] + c[(i+1)%N] + c[(i+2)%N];
        ans.chmax(u);
    }

    println!("{}", ans);

    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
