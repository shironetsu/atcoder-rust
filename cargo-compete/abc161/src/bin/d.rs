#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        K: usize,
    }

    let mut todo = VecDeque::new();
    for i in 1..=9 {
        todo.push_back(vec![i]);
    }

    for k in 1.. {
        let v = todo.pop_front().unwrap(); 
        if k == K {
            let ans = v.iter().map(|x|(b'0' + x) as char).collect::<String>();
            println!("{}", ans);
            return;
        } else {
            let &l = v.last().unwrap();
            for &x in [l+!0,l,l+1].iter().filter(|&&x|0<=x && x<=9){
                let mut w = v.clone();
                w.push(x);
                todo.push_back(w);
            }
        }
    }

    
}
