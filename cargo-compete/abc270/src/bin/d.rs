#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        mut N: i64,
        K: usize,
        A: [i64;K],
    }

    let mut a = vec![0, 0];
    for i in 0..{
        let k = A.lower_bound(&N);
        if k < K && A[k] == N {
            a[i&1] += N;
            break;
        } else {
            a[i&1] += A[k-1];
            N -= A[k-1];
        }
    }

    println!("{}", a[0]);
    
}
