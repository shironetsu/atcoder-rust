#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
        sc: [(Usize1, i32);M],
    }

    for n in 0..=999{
        let a = format!("{}", n).chars().map(|c|(c as u8 - b'0') as i32).collect_vec();
        if a.len() != N {
            continue;
        }
        let ok = sc.iter().map(|&(s, c)|a[s]==c).fold(true, |acc, x|acc&&x);
        if ok {
            println!("{}", n);
            return;
        }
    }
    println!("{}", -1);

    
}
