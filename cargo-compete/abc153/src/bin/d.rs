fn rec(x: i64, memo: &mut BTreeMap<i64, i64>)->i64{
    if let Some(&y) = memo.get(&x) {
        y
    } else {
        let u = rec(x/2, memo);
        memo.insert(x, 2 * u);
        2 * u + 1
    }
}

#[fastout]
fn main() {
    input!{
        H: i64,
    }

    let mut memo = btreemap![
        1 => 1
    ];
    let ans = rec(H, &mut memo);

    println!("{}", ans);
}
