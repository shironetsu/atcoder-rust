#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        S: [String;N],
    }

    let mut dp = vec![vec![0i64;2];N+1];
    dp[0] = vec![1;2];
    for i in 0..N{
        match S[i].as_str() {
            "AND" => {
                dp[i+1][0] += 2*dp[i][0] + dp[i][1];
                dp[i+1][1] += dp[i][1];
            }, 
            "OR" => {
                dp[i+1][0] += dp[i][0];
                dp[i+1][1] += dp[i][0] + 2*dp[i][1];
            },
            _ => unreachable!(),
        }
    }

    println!("{}", dp[N][1]);
    
}
