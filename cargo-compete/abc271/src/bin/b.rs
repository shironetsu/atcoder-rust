#[fastout]
fn main() {
    input!{
        N: usize,
        Q: usize,
    }

    let mut a = vec![vec![];N];
    let mut L = vec![0;N];
    for i in 0..N{
        input!{
            l: usize,
        }
        L[i] = l;
        input!{
            aa: [i64;l],
        }
        a[i] = aa;
    }

    for i in 0..Q{
        input!{
            s: Usize1,
            t: Usize1
        }
        println!("{}", a[s][t]);
    }

    
}
