#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        H: usize,
        W: usize,
        S: [Chars;H],
        T: [Chars;H],
    }

    let mut a = BTreeMap::<String, usize>::new();
    let mut b = BTreeMap::<String, usize>::new();
    for j in 0..W{
        let s = (0..H).map(|i|S[i][j]).collect::<String>();
        let t = (0..H).map(|i|T[i][j]).collect::<String>();
        *a.entry(s).or_default() += 1;
        *b.entry(t).or_default() += 1;
    }

    if a == b {
        println!("Yes");
    } else {
        println!("No");
    }
}
