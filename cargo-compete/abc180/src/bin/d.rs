#[fastout]
fn main() {
    input!{
        mut X: i128,
        Y: i128,
        A: i128,
        B: i128,
    }

    let mut ans = 0i128;
    //while X * A <= X + B 
    // <=> X <= B / (A-1) 
    // A
    while X * A < Y && X * A <= X + B {
        X *= A;
        ans += 1;
    }

    // X + k B < Y
    // <=> k < (Y-X) / B
    let kmax = if (Y-X) % B == 0 {
        (Y-X) /  B - 1
    } else {
        (Y-X) / B
    };

    ans += kmax.max(0);
    println!("{}", ans);

}
//______________________________________________________________________________
