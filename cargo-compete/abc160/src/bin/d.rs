#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        X: Isize1,
        Y: Isize1,
    }

    let mut ans = vec![0;N];
    for i in 0..N as isize{
        for j in i+1..N as isize{
            let a = (i-j).abs();
            let b = (i-X).abs() + 1 + (j-Y).abs();
            let d = a.min(b);
            ans[d as usize] += 1;
        }
    }

    for i in 1..=N-1{
        println!("{}", ans[i]);
    }
    
}
