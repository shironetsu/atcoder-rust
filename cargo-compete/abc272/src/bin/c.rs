#[fastout]
fn main() {
    input!{
        N: usize,
        A: [i64;N],
    }

    let mut even = A.iter().copied().filter(|&x|x&1==0).collect_vec();
    let mut odd = A.iter().copied().filter(|&x|x&1==1).collect_vec();

    if even.len() < 2 && odd.len() < 2 {
        println!("{}", -1);
        return;
    }

    even.sort();
    even.reverse();
    odd.sort();
    odd.reverse();

    let mut ans = 0;
    
    if even.len() >= 2 {
        ans.chmax(even[0]+even[1]);
    }

    if odd.len() >= 2 {
        ans.chmax(odd[0]+odd[1]);
    }

    println!("{}", ans);


    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
