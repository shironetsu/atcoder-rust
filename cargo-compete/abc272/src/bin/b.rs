#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
    }

    let mut ok = vec![vec![false;N];N];
    for _ in 0..M{
        input!{
            k: usize,
            x: [Usize1;k],
        }

        for i in 0..k{
            for j in i+1..k{
                ok[x[i]][x[j]] = true;
            }
        }
    }

    for i in 0..N{
        for j in i+1..N{
            if !ok[i][j] {
                println!("No");
                return;
            }
        }
    }

    println!("Yes");

    
}
