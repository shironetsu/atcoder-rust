#[fastout]
fn main() {
    input!{
        N :usize,
        AB: [(i64, i64);N],
    }
    let A = AB.iter().map(|&ab|ab.0).collect_vec();
    let B = AB.iter().map(|&ab|ab.1).collect_vec();

    let mut ans = 10i64.pow(18);
    for i in 0..N{
        for j in 0..N{
            let t = if i == j {
                A[i] + B[i]
            } else {
                A[i].max(B[j])
            };
            ans.chmin(t);
        }
    }
    println!("{}", ans);


    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
