#[fastout]
fn main() {
    input!{
        A: usize,
        B: usize,
    }

    let mut inf = 1_000;
    let mut t = vec![vec![inf;B+1];A+1];
    for y in (0..=200).rev(){
        let a = y * 108 / 100 - y;
        let b = y * 110 / 100 - y;
        if a <= A && b <= B {
            t[a][b] = y;
        }
    }

    if t[A][B] < inf {
        let mut y = t[A][B];
        if y == 0 {
            y += 100;
        }
        println!("{}", y);
    } else {
        println!("{}", -1);
    }

    
}
