#[fastout]
fn main() {
    input!{
        A: f64,
        B: f64,
        H: f64,
        M: f64,
    }

    let a = 2.0*std::f64::consts::PI * (H*60.0+M) / (12.0*60.0);
    let b = 2.0*std::f64::consts::PI * M / 60.0;
    let ans = (A.powi(2) + B.powi(2) - 2.0*A*B*((b-a).cos())).sqrt();
    println!("{}", ans);
}
