#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        A: [i64;1<<N]
    }

    let mut A = A.into_iter().enumerate().map(|p|(p.1, p.0)).collect_vec();

    for i in 0..N-1{
        let mut B = vec![(0,0);1<<(N-i-1)];
        for i in 0..1<<(N-i-1){
            B[i] = A[2*i].max(A[2*i+1]);
        }
        A = B;
    }

    let ans = A[0].min(A[1]).1 + 1;
    println!("{}", ans);

    
}
//______________________________________________________________________________
