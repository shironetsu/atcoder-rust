#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        A: [Usize1;N],
    }

    let mut cnt = vec![0;N];
    for &a in A.iter(){
        cnt[a] += 1;
    }
    let sum = cnt.iter().map(|&c|{
        let c = c as i64;
        c * (c-1) / 2
    }).sum::<i64>();
    let mut ans = vec![0;N];
    for i in 0..N{
        ans[i] = sum - (cnt[A[i]]-1);
    }
    for &a in ans.iter(){
        println!("{}", a);
    }

    
}
