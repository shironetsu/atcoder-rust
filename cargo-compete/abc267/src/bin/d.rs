#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
        A: [i64;N],
    }

    let mut dp = vec![vec![-10i64.pow(18);M+1];N+1];
    dp[0][0] = 0;

    for i in 0..N{
        dp[i+1] = dp[i].clone();
        for j in 0..M{
            //println!("{}{}", i, j);
            let x = dp[i][j] + (j as i64 + 1) * A[i];
            dp[i+1][j+1].chmax(x);
        }
    }

    //println!("{:?}", dp);

    println!("{}", dp[N][M]);
    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
