#[fastout]
fn main() {
    input!{
        N: usize,
        M: usize,
        A: [i64;N],
    }

    let mut AA = vec![0;N+1];
    for i in 0..N{
        AA[i+1] = AA[i] + A[i];
    }
    let C = A.clone().into_iter().enumerate().map(|(i, x)| i as i64 * x).collect_vec();
    let mut CC = vec![0;N+1];
    for i in 0..N{
        CC[i+1] = CC[i] + C[i];
    }

    let mut ans = std::i64::MIN;
    for k in 0..=N-M{
        let sum = CC[k+M]-CC[k] 
                        - (k as i64 - 1) * (AA[k+M]-AA[k]);
        ans.chmax(sum);
    }

    println!("{}", ans);

    
}
pub trait Change<T: PartialOrd> {
    fn chmin(&mut self, rhs: Self) -> bool;
    fn chmax(&mut self, rhs: Self) -> bool;
}
impl<T: PartialOrd> Change<T> for T {
    fn chmax(&mut self, rhs: Self) -> bool {
        if *self < rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
    fn chmin(&mut self, rhs: Self) -> bool {
        if *self > rhs {
            *self = rhs;
            true
        } else {
            false
        }
    }
}
