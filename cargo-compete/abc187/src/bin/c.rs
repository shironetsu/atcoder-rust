#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        S: [Chars;N],
    }

    let mut t = BTreeSet::new();
    let mut f = BTreeSet::new();
    for i in 0..N{
        if S[i][0] == '!' {
            let s = S[i].clone().into_iter().skip(1).collect_vec();
            f.insert(s);
        } else {
            t.insert(S[i].clone());
        }
    }

    let mut iter = t.intersection(&f).clone();
    if let Some(ans) = iter.next(){
        println!("{}", ans.iter().collect::<String>());
    } else {
        println!("{}", "satisfiable");
    }

    
}
