#![allow(unused_imports)]
#![allow(non_snake_case)]
use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        xy: [(i64,i64);N],
    }

    let mut ans = 0;
    for i in 0..N{
        for j in i+1..N{
            let (x0, y0) = xy[i];
            let (x1, y1) = xy[j];
            let mut dx = x1-x0;
            let mut dy = y1-y0;
            if dx == 0 {
                continue;
            }
            if dx < 0 {
                dx *= -1;
                dy *= -1;
            }
            if -dx <=dy && dy <= dx {
                ans += 1;
            }
        }
    }

    println!("{}", ans);
}
//______________________________________________________________________________
//
pub trait Answer {
    fn fmt(&self)->String;
    fn fmtl(&self)->String;
    fn ans(&self);
    fn ansl(&self);
}

impl<T: Display> Answer for Vec<T> {
    fn fmt(&self)->String {
        self
            .iter()
            .map(|x| format!("{}", x))
            .collect::<Vec<_>>()
            .join(" ")
    }

    fn ans(&self) {
        println!("{}", self.fmt());
    }

    fn fmtl(&self)->String {
        self
            .iter()
            .map(|x| format!("{}", x))
            .collect::<Vec<_>>()
            .join("\n")
    }

    fn ansl(&self) {
        println!("{}", self.fmtl());
    }
}
