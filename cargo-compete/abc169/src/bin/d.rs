#[fastout]
fn main() {
    input!{
        mut N: i64,
    }

    let mut m = BTreeMap::<i64,isize>::new();
    for d in 2..=num_integer::sqrt(N){
        while N % d == 0 {
            N /= d;
            *m.entry(d).or_default() += 1;
        }
    }

    if N > 1 {
        m.insert(N, 1);
    }

    let mut ans = 0;
    for (&p, k) in m.iter(){
        let n = (-1+num_integer::sqrt(1+8*k))/2;
        ans += n;
    }

    println!("{}", ans);





    
}
