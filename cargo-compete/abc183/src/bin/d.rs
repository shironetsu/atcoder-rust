use proconio::{input, fastout};
use proconio::marker::{Bytes, Chars, Isize1, Usize1};
use std::fmt::{Write, Display};
use std::collections::*;
use maplit::*;
use itertools::*;
use superslice::{Ext, Ext2};

#[fastout]
fn main() {
    input!{
        N: usize,
        W: i64,
        STP: [(usize,usize,i64);N],
    }

    let M = 2 * 10usize.pow(5);
    let mut imos = vec![0;M+1];
    for &(s, t, p) in STP.iter(){
        imos[s] += p;
        imos[t] -= p;
    }

    for i in 1..=M{
        imos[i] += imos[i-1];
    }

    let max = imos.into_iter().max().unwrap();
    if max > W {
        println!("No");
    } else {
        println!("Yes");
    }

    
}
