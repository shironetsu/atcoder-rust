#[fastout]
fn main() {
    input!{
        N: i64,
    }

    let mut ans = 0i64;
    let mut l = 1i64;
    let mut c = 0i64;
    while l <= N {
        let r = l * 1000;
        ans += c * (r.min(N+1) - l);
        c += 1;
        l = r;
    }
    println!("{}", ans);

    
}
